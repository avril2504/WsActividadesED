package Actividad13;

public class Matematica {
	
	/**
	 * Método que suma dos números decimales pasados por parametro
	 * @param n1 primer número que se introduce y el cual se encuentra en la primera
	 * posición
	 * @param n2 segundo número que se introduce y el cual se encuentra en la segunda
	 * posición
	 * @return devuelve el resultado de la suma entre los dos numeros pasados por
	 * parametro 
	 */
	public static double suma(double n1, double n2) {
		return n1 + n2;
	}
	
	/**
	 * Método que resta dos números decimales pasados por parametro
	 * @param n1 primer número que se introduce y el cual se encuentra en la primera
	 * posición
	 * @param n2 segundo número que se introduce y el cual se encuentra en la segunda
	 * posición
	 * @return devuelve el resultado de la resta entre los dos numeros pasados por
	 * parametro 
	 */
	public static double resta(double n1, double n2) {
		return n1 - n2;
	}
	
	/**
	 * Método que multiplica dos números decimales pasados por parametro
	 * @param n1 primer número que se introduce y el cual se encuentra en la primera
	 * posición
	 * @param n2 segundo número que se introduce y el cual se encuentra en la segunda
	 * posición
	 * @return devuelve el resultado de la multiplicación entre los dos numeros pasados por
	 * parametro 
	 */
	public static double multiplicacion(double n1, double n2) {
		return n1 * n2;
	}
	
	/**
	 * Método que divide dos números decimales pasados por parametro
	 * @param n1 primer número que se introduce y el cual se encuentra en la primera
	 * posición
	 * @param n2 segundo número que se introduce y el cual se encuentra en la segunda
	 * posición
	 * @return devuelve el resultado de la division entre los dos numeros pasados por
	 * parametro 
	 */
	public static double division(double n1, double n2) {
		return n1 / n2;
	}

}
